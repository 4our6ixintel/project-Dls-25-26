// ==== LOAD WEEK ====
function loadWeek() {
  const week = document.getElementById("week").value;
  const container = document.getElementById("match-container");
  container.innerHTML = "";

  if (!matches[week] || matches[week].length === 0) {
    container.innerHTML = `<p style="text-align:center;margin-top:2rem;">No matches scheduled.</p>`;
    return;
  }

  matches[week].forEach(day => {
    const section = document.createElement("section");
    section.classList.add("match-day");
    let html = `<h2>${day.day} â€” <small>${day.date}</small></h2><div class="projects">`;

    day.games.forEach(game => {
      html += `
        <div class="project-card" onclick="openModal('${game.title}', '${game.teams}', '${game.time}', '${game.link}')">
          <h3>${game.title}</h3>
          <p>${game.teams}</p>
          <p><strong>Start:</strong> ${game.time}</p>
        </div>`;
    });

    html += `</div>`;
    section.innerHTML = html;
    container.appendChild(section);
  });
}

// ==== MODAL HANDLERS ====
function openModal(title, teams, time, link) {
  document.getElementById("modal-title").textContent = title;
  document.getElementById("modal-teams").textContent = teams;
  document.getElementById("modal-time").textContent = "Start Time: " + time;
  document.getElementById("modal-link").href = link;

  startCountdown(time);
  document.getElementById("modal").style.display = "flex";
}

function closeModal() {
  document.getElementById("modal").style.display = "none";
}

// ==== COUNTDOWN TIMER ====
function startCountdown(time) {
  const countdownEl = document.getElementById("modal-countdown");

  const [hour, minute] = time.split(":").map(Number);
  const matchTime = new Date();
  matchTime.setHours(hour, minute, 0);

  function update() {
    const now = new Date();
    const diff = matchTime - now;
    if (diff <= 0) {
      countdownEl.textContent = "Match is Live!";
      return;
    }
    const hrs = Math.floor(diff / (1000 * 60 * 60));
    const mins = Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60));
    const secs = Math.floor((diff % (1000 * 60)) / 1000);
    countdownEl.textContent = `Starts in ${hrs}h ${mins}m ${secs}s`;
    requestAnimationFrame(update);
  }
  update();
}

